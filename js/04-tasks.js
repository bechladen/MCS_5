const courses = ['HTML', 'CSS', 'JavaScript', 'React', 'PostgreSQL'];

function addCourse(courseName) {
  if (courses.includes(courseName)) {
    console.error(`${courseName} –≤–∂–µ —ñ—Å–Ω—É—î —É —Å–ø–∏—Å–∫—É –∫—É—Ä—Å—ñ–≤!`);
    return;
  }
  courses.push(courseName);
}

function removeCourse(courseName) {
  const indexToDelete = courses.indexOf(courseName);

  if (indexToDelete === -1) {
    console.error(`–ö—É—Ä—Å—É ${courseName} –Ω–µ —ñ—Å–Ω—É—î —É —Å–ø–∏—Å–∫—É –∫—É—Ä—Å—ñ–≤!`);
    return;
  }

  courses.splice(indexToDelete, 1);
}

function updateCourse(oldCourseName, newCourseName) {
  const indexToUpdate = courses.indexOf(oldCourseName);

  if (indexToUpdate === -1) {
    console.error(`–ö—É—Ä—Å—É ${oldCourseName} –Ω–µ —ñ—Å–Ω—É—î —É —Å–ø–∏—Å–∫—É –∫—É—Ä—Å—ñ–≤!`);
    return;
  }

  courses.splice(indexToUpdate, 1, newCourseName);
}

